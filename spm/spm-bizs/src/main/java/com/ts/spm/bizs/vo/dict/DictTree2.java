package com.ts.spm.bizs.vo.dict;

import com.github.wxiaoqi.security.common.vo.TreeNodeVO;

import java.util.Date;

/**
 * @author ace
 * @create 2018/1/30.
 */
public class DictTree2 extends TreeNodeVO<DictTree2> {
    String code;
    String name;
    String orderNum;
    Date crtTime;

    public DictTree2(){

    }

    public DictTree2(Object id, Object parentId, String name, String code, String orderNum, Date crtTime) {
        this.code = code;
        this.name = name;
        this.orderNum = orderNum;
        this.crtTime = crtTime;
        this.setId(id);
        this.setParentId(parentId);
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOrderNum() {
        return orderNum;
    }

    public void setOrderNum(String orderNum) {
        this.orderNum = orderNum;
    }

    public Date getCrtTime() {
        return crtTime;
    }

    public void setCrtTime(Date crtTime) {
        this.crtTime = crtTime;
    }
}
